-- Enable Haiku tier for Whale Class
DO $$
DECLARE
  whale_school_id UUID;
BEGIN
  SELECT school_id INTO whale_school_id
  FROM montree_classrooms
  WHERE id = '51e7adb6-cd18-4e03-b707-eceb0a1d2e69'
  LIMIT 1;

  IF whale_school_id IS NOT NULL THEN
    INSERT INTO montree_school_features (school_id, feature_key, enabled, enabled_by)
    VALUES (whale_school_id, 'ai_tier_haiku', true, 'migration_181')
    ON CONFLICT (school_id, feature_key) DO UPDATE SET enabled = true, enabled_by = 'migration_181';

    -- Ensure Sonnet is explicitly OFF for Whale Class so precedence resolution
    -- is crystal clear and doesn't depend on row absence.
    INSERT INTO montree_school_features (school_id, feature_key, enabled, enabled_by)
    VALUES (whale_school_id, 'ai_tier_sonnet', false, 'migration_181')
    ON CONFLICT (school_id, feature_key) DO UPDATE SET enabled = false, enabled_by = 'migration_181';
  END IF;
END $$;
