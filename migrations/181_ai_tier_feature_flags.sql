-- Migration 181: AI report tier flags (free / haiku / sonnet)
--
-- Defines two feature flags that control which Anthropic model powers a
-- school's weekly Teacher Report + Parent Narrative generation:
--
--   ai_tier_haiku   → claude-haiku-4-5    ("Montree Standard" — cheap tier)
--   ai_tier_sonnet  → claude-sonnet-4-6   ("Montree Premium"  — high tier)
--
-- Neither flag = free tier (no AI calls, reports endpoint returns 402).
-- Sonnet takes precedence over Haiku if somehow both are enabled.
--
-- Resolution happens per-request in lib/montree/reports/resolve-model.ts
-- and is wired into app/api/montree/reports/weekly-wrap/route.ts.
--
-- Both default OFF for safety. Whale Class is enabled on Haiku immediately.

INSERT INTO montree_feature_definitions (
  feature_key, name, description, icon, category, is_premium, default_enabled
) VALUES (
  'ai_tier_haiku',
  'AI Reports — Standard',
  'Weekly Teacher Reports + Parent Narratives generated by Claude Haiku. Fast, affordable AI tier. Target pricing: ~$4/student/month.',
  '⚡',
  'ai_tier',
  true,
  false
) ON CONFLICT (feature_key) DO UPDATE SET
  name = EXCLUDED.name,
  description = EXCLUDED.description,
  icon = EXCLUDED.icon,
  category = EXCLUDED.category,
  is_premium = EXCLUDED.is_premium;

INSERT INTO montree_feature_definitions (
