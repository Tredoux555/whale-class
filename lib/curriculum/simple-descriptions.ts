// Fallback for works not in the list
export function getSimpleDescription(workName: string): { line1: string; line2: string } | null {
  // Try exact match
  if (SIMPLE_DESCRIPTIONS[workName]) {
    return SIMPLE_DESCRIPTIONS[workName];
  }
  
  // Try case-insensitive match
  const lowerName = workName.toLowerCase();
  for (const [key, value] of Object.entries(SIMPLE_DESCRIPTIONS)) {
    if (key.toLowerCase() === lowerName) {
      return value;
    }
  }
  
  // Try partial match (work name contains key or key contains work name)
  for (const [key, value] of Object.entries(SIMPLE_DESCRIPTIONS)) {
    if (key.toLowerCase().includes(lowerName) || lowerName.includes(key.toLowerCase())) {
      return value;
    }
  }
  
  return null;
}
