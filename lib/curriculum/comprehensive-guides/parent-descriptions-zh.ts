/**
 * Get Chinese parent description for a work by its English name.
 * Returns undefined if no Chinese translation exists for this work.
 */
export function getChineseParentDescription(workName: string): ParentDescriptionZh | undefined {
  return DESCRIPTIONS[workName.toLowerCase().trim()];
}

/**
 * Build a Map<lowercase_name, ParentDescriptionZh> for batch lookups.
 * Used by route files to override English description maps in one pass.
 */
export function getChineseDescriptionsMap(): Map<string, ParentDescriptionZh> {
  const map = new Map<string, ParentDescriptionZh>();
  for (const [key, value] of Object.entries(DESCRIPTIONS)) {
    map.set(key, value);
  }
  return map;
}
